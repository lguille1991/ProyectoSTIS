    public DefaultTableModel mostrar(){
        this.jTxtIdTorneo.setVisible(false);
        this.jTxtIdLocal.setVisible(false);
        this.jTxtIdVisitante.setVisible(false);
        this.jTxtIdEstadio.setVisible(false);
        this.jTxtIdArbitro.setVisible(false);
        this.jTxtIdJornada.setVisible(false);
        String []columnas={"Código","Torneo","Local","Visitante","Estadio","Árbitro","Fecha","Horario","Día","Hora","Jornada"};
        Object[]obj=new Object[11];
        DefaultTableModel tabla = new DefaultTableModel(null,columnas);
        Partido pa = new Partido();
        ControlPartido cp = new ControlPartido();
        List ls;
        try{
            ls=cp.mostrarPartido();
            for(int i=0;i<ls.size();i++){
                pa=(Partido)ls.get(i);
                obj[0]=pa.getIdPartido();
                obj[1]=pa.getNombreTorneo();
                obj[2]=pa.getNombreEquipoLocal();
                obj[3]=pa.getNombreEquipoVisitante();
                obj[4]=pa.getEstadio();
                obj[5]=pa.getNombreArbitro();
                obj[6]=pa.getFecha();
                obj[7]=pa.getIdHorario();
                obj[8]=pa.getDia();
                obj[9]=pa.getHora();
                obj[10]=pa.getNombreJornada();
                tabla.addRow(obj);
            }
            ls=cp.mostrarPartido();
            this.jTablaPartido.setModel(tabla);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al mostrar datos" + e.toString());
        }
        return tabla;
    }
    
