    private void BtnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnEditarMouseClicked
        if(this.jTxtCodigo.getText().equals("")&&this.jTxtFecha.getText().equals("")&&this.jComboTorneo.getSelectedItem().toString().equals("Seleccione uno")&&
            this.jComboLocal.getSelectedItem().toString().equals("Seleccione uno")&&this.jComboVisita.getSelectedItem().toString().equals("Seleccione uno")&&
            this.jComboEstadio.getSelectedItem().toString().equals("Seleccione uno")&&this.jComboArbitro.getSelectedItem().toString().equals("Seleccione uno")&&
            this.jComboHorario.getSelectedItem().toString().equals("Seleccione uno")&&this.jComboJornada.getSelectedItem().toString().equals("Seleccione uno")){//No permite editar sin haber seleccionado un registro
            JOptionPane.showMessageDialog(rootPane, "Por favor seleccione un registro a editar", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            habilitarInput(true,false);
            editar=1;
        }
    }//GEN-LAST:event_BtnEditarMouseClicked

    private void BtnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnGuardarMouseClicked
        if(nuevo==1 && this.jTxtFecha.getText().equals("")&&this.jComboTorneo.getSelectedItem().toString().equals("Seleccione uno")&&
            this.jComboLocal.getSelectedItem().toString().equals("Seleccione uno")&&this.jComboVisita.getSelectedItem().toString().equals("Seleccione uno")&&
            this.jComboEstadio.getSelectedItem().toString().equals("Seleccione uno")&&this.jComboArbitro.getSelectedItem().toString().equals("Seleccione uno")&&
            this.jComboHorario.getSelectedItem().toString().equals("Seleccione uno")&&this.jComboJornada.getSelectedItem().toString().equals("Seleccione uno")){//No permite guardar si el campo está vacío
            JOptionPane.showMessageDialog(rootPane, "Complete los campos requeridos", "ERROR", JOptionPane.ERROR_MESSAGE);
            habilitarInput(true,false);
        }else{
            if(editar==0 && nuevo==1){
                insertar();
                habilitarInput(false,false);
            }else if(editar==1){
                modificar();
                habilitarInput(false,false);
            }else if(nuevo==0 && this.jTxtFecha.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Clic en NUEVO para ingresar un gol", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if(editar==0 && nuevo==0){
                JOptionPane.showMessageDialog(rootPane, "Clic en EDITAR para guardar los cambios ó en NUEVO para crear un nuevo registro", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnGuardarMouseClicked

